package com.scipionyx.butterflyeffect.configuration.model.menu;

/**
 * 
 * 
 * 
 * @author Renato Mendes
 *
 */
public class ConfigurationMenuItem {

	private String name;

	private String display;

	private String viewName;

	private String addSeparatorAfter;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDisplay() {
		return display;
	}

	public void setDisplay(String display) {
		this.display = display;
	}

	public String getViewName() {
		return viewName;
	}

	public void setViewName(String viewName) {
		this.viewName = viewName;
	}

	public String getAddSeparatorAfter() {
		return addSeparatorAfter;
	}

	public void setAddSeparatorAfter(String addSeparatorAfter) {
		this.addSeparatorAfter = addSeparatorAfter;
	}

}
